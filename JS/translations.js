const GF_TRANSLATION_KEYS = Object.keys(GF_LANGS.nl).filter(k => !k.startsWith('_'));

let _gfTLang = null;
try { chrome.storage?.sync?.get('gf-lang', r => { _gfTLang = r?.['gf-lang'] || null; }); } catch (_) {}
try {
  chrome.storage?.onChanged?.addListener((changes, area) => {
    if (area === 'sync' && changes['gf-lang']) _gfTLang = changes['gf-lang'].newValue || null;
  });
} catch (_) {}
function _GfTranslate(key) {
  if (typeof GF_LANGS === 'undefined') return key;
  let code = _gfTLang;
  if (!code || code === 'auto' || code === 'custom') {
    const nav = (navigator.language || 'nl').split('-')[0].toLowerCase();
    code = GF_LANGS[nav] ? nav : 'nl';
  }
  if (!GF_LANGS[code]) code = 'nl';
  return GF_LANGS[code]?.[key] ?? GF_LANGS['nl']?.[key] ?? key;
}
